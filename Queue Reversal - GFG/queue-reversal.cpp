//Function to reverse the queue.
class Solution
{
    public:
    queue<int> rev(queue<int> q)
    {
        // add code here.
        vector<int> store;
        while(!q.empty()){
           store.push_back(q.front());
           q.pop();
        }
        reverse(store.begin(),store.end());
        for(int i=0;i<store.size();i++){
            q.push(store[i]);
        }
        return q;
    }
};


